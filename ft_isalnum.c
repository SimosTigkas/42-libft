#include "libft.h"

int	ft_isdigit(int c)
{
	if (c > 48 && c < 58)
		return (1);
	else
		return (0);
}

int	ft_isalpha(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
		return (1);
	else
		return (0);
}

int	ft_isalnum(int c)
{
	if (ft_isalpha(c) || ft_isdigit(c) == 1)
		return (1);
	else
		return (0);
}
